package modelo.getset;

public class Touro extends Animal {
    private String nomeTouro;
    private String racaTouro;
    private String registro;
    private String nomeCentral;


    public String getNomeTouro() {
        return nomeTouro;
    }


    public void setNomeTouro(String nomeTouro) {
        this.nomeTouro = nomeTouro;
    }


    public String getRacaTouro() {
        return racaTouro;
    }


    public void setRacaTouro(String racaTouro) {
        this.racaTouro = racaTouro;
    }

    public String getRegistro() {
        return registro;
    }

    public void setRegistro(String registro) {
        this.registro = registro;
    }

    public String getNomeCentral() {
        return nomeCentral;
    }

    public void setNomeCentral(String nomeCentral) {
        this.nomeCentral = nomeCentral;
    }
}
